                        <h1>Introduction of L.E. College</h1>
                        <p>Lukhdhirji Engineering College was established as a Technical Institute in 1931, then known as Morvi Technical Institute (MTI) and was upgraded to a full-fledged polytechnic and degree engineering college. In 1951 the Late Honorable H.H. Maharaja Thakore Shri Sir Lukhdhirji Waghji Sahib of Morvi state donated his palace with 40 acres (160,000 m2) of land on the bank of river Machhu so the institute was named after him. The institute is operated by Commissionerate of Technical Education (CTE), the Government of Gujarat and presently is affiliated with Gujarat Technological University. At present offers 3 year Diploma Courses in 05 different programs. Located in the outskirt of the city and remains the first choice for admission among students of the region. Previously, the institute was functioning from old campus along with Lukhdhirji Engineering College (Degree) courses, Morbi. In June 2018, the Government allotted new campus for Diploma studies. So, the institute was shifted to the new campus that has all required infrastructure for teaching and other activities.

                            College offers diploma courses in disciplines like Civil, Mechanical, Electrical, Ceramic Technology, Metallurgy, Electronics & Communication and Information Technology Engineering. The institute is approved by AICTE and affiliated with Gujarat Technological University, Ahmedabad, Gujarat

